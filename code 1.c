/*Program to get two integers from the user and print the sum of those two integers*/
#include <stdio.h> /*Global declaration*/
/* Function main begins program execution */
int main()
{
 
int a,b,c; /*Local variable declaration*/
    
printf("Enter the First integer: ");
/*prompt*/
    
scanf("%i",&a);/*read an integer*/
    
printf("Enter the second integer:");
/*prompt*/
        
 scanf("%i",&b);/*read an integer*/
    
c = a+b;/* assign total to c */
    
printf("Sum of the above two integers is --> \n%i",c); /*print sum*/
return 0;
/* indicate that program ended successfully */
    
}/* end function main */
